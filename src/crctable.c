/*
 * Hardware-accelerated CRC-32 using PCLMULQDQ (when available).
 * Defined in crc32_pclmul.c, compiled only when __PCLMUL__ is set.
 */
#ifdef __PCLMUL__
extern uint32_t crc32_pclmul(uint32_t crc, const uint8_t *buf, uint32_t len);
#endif

/*
 * Compute CRC32 over a buffer. Uses PCLMULQDQ hardware acceleration
 * for buffers >= 64 bytes when available, falling back to slicing-by-8.
 */
UInt32 BZ2_crc32_update ( UInt32 crc, const UChar* buf, UInt32 len )
{
#ifdef __PCLMUL__
   if (len >= 64)
      return (UInt32)crc32_pclmul((uint32_t)crc, (const uint8_t *)buf, (uint32_t)len);
#endif
   const UChar* p = buf;
   const UChar* end = buf + len;

   /* Byte-at-a-time until 4-byte aligned */
   while (p < end && ((uintptr_t)p & 3)) {
      crc = (crc << 8) ^ BZ2_crc32Table[(crc >> 24) ^ *p++];
   }

   /* Slicing-by-8 main loop */
   while (p + 8 <= end) {
      uint32_t one, two;
      memcpy(&one, p, 4);
      memcpy(&two, p + 4, 4);
      one ^= __builtin_bswap32(crc);
      one = __builtin_bswap32(one);
      two = __builtin_bswap32(two);
      crc = crc32_s8[7][(one >> 24) & 0xFF] ^
            crc32_s8[6][(one >> 16) & 0xFF] ^
            crc32_s8[5][(one >>  8) & 0xFF] ^
            crc32_s8[4][ one        & 0xFF] ^
            crc32_s8[3][(two >> 24) & 0xFF] ^
            crc32_s8[2][(two >> 16) & 0xFF] ^
            crc32_s8[1][(two >>  8) & 0xFF] ^
            crc32_s8[0][ two        & 0xFF];
      p += 8;
   }

   /* Byte-at-a-time tail */
   while (p < end) {
      crc = (crc << 8) ^ BZ2_crc32Table[(crc >> 24) ^ *p++];
   }

   return crc;
}

